						        most likely try to get you down on the price, and buyers don’t talk in hundreds, it’s always thousands.
						    </p>
						    <p>In addition to minor repairs, you also want to de-clutter. We tell all of our clients, you have to have it empty before closing anyway so better to get a head start in the beginning because it will help the property show better.</p>
