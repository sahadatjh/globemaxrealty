						        Congratulations, you have your first accepted offer! But don’t crack open that wine bottle yet because you still have a lot of work to do. First off, you have to schedule the buyer’s home inspection as soon as possible. Sometimes
						        buyers drag their feet at this point so you want to create urgency and make sure that they move fast. Being that you probably have multiple back ups, this should not be difficult. If for some reason you don’t have multiple back ups,
						        then you don’t have much leverage and may just have to wait for them.
						    </p>
						    <p><em>For Coops: Once offer is accepted, it is time to conduct a full financial review.&nbsp;</em></p>
						    <p>
						        If you just accepted an offer on your Coop, then what you need to do next is a full financial review. This is non-negotiable because not only do they need to get passed the management company, but more importantly, they need to get
						        approved by the board. So, you just need to make sure they look good on paper and don’t have any circumstances or situations that will raise eyebrows when the pacakage is presented. Here’s what you need to request and look at for
						        the financial review:
						    </p>
						    <ul>
						        <li>First page of last two yrs tax returns – To verify adjusted gross income for past two years.</li>
						        <li>Last two yrs W-2s, 1099s, K-1s, etc – To verify gross wages and employment history.</li>
						        <li>Last month pay stubs – To verify current employer and base pay.</li>
						        <li>Current position &amp; length of employment – Not a formal letter of employment, you just need to know their current position and length of employment with the current Company.</li>
						        <li>Full credit report – To calculate Debt-to-Income ratio</li>
						        <li>Proof of funds – To verify down pmt as well as reserves to show after the sale.</li>
						    </ul>
						    <p>
						        If anything here looks off or concerning to you, you can almost guarantee that it would do the same when presented to the board. The highest offer is not always the best candidate, and when it comes to selling Coops, you ALWAYS want
						        to go with the best candidate. If for some reason they don’t meet specific Coop requirements, or if they have some items in their financial review that can be a cause for concern, then you may want to go on to the first back up
						        offer and perform the financial review for them. Repeat these steps until you have an acceptable offer price with solid financials from a qualified candidate.
						    </p>
