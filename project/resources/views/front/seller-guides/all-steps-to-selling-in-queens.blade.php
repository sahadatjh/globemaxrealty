						    <p>
						        Once you’ve decided to sell, now you need to get your things in order. A lot of these Queens’ houses are very old, and depending on whether you inherited the property or purchased it, there are a lot of documents that you want to
						        gather in order to streamline the sale. You definitely want to locate your old title report, your property deed, any mortgage records, payoff statements, ownership/transfer documents, violation documents, lien documents, wills,
						        administration/probate documents, surveys, certificate of occupancies, etc.
						    </p>
						    <p>
						        Tips: If you are handling an estate sale, you will need to make sure that the proper probate or administration proceeding is filed with the surrogates court before listing the house. If the person who passed away lived out of state
						        and the probate was done in that state, you will still need to file an ancillary probate in New York State so you will need to get that filed before listing. If you are a non-resident of New York, you will need to pay capital gains
						        at closing so you should get an idea of the amount from a local CPA. If you have tenants, you should notify them of the sale asap and if they don’t have a lease or the lease has ended, we would advise sending a notice of termination
						        and starting a holdover case if necessary.
