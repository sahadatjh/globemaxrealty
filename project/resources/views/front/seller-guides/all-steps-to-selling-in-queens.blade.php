						    <h3>Step 6</h3>
						    <h3>Make Repairs and Declutter</h3>
						    <p>
						        Once you have your pre inspection done, the next step should be to take care of any minor/affordable fixes. You obviously are not going to take care of big issues like roofing, plumbing, electrical, etc. but if you have some small
