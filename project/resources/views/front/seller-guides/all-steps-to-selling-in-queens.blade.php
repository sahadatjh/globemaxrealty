						    <h3>Step 22</h3>
						    <h3>Contract is sent to Buyer’s attorney</h3>
						    <p>
						        Once the contract is drawn up, it is then sent over to the buyer’s attorney who will review the contract and most likely make some revisions. Prepare for the attorneys to go back and forth because they have to agree on all the
						        terms, conditions, and wording in the actual contract. Sometimes they can take a while just push the deal if they’re taking too long. If there are any major contract items that need addressing or were not part of the original
						        agreement, you will know and most likely need to get everyone back on the same page. Either way, the idea here is to make sure nobody kills the deal with petty requests or legal jargon that nobody is in agreement with. The good news
						        is that typically Attorneys are able to work together to come to an agreement on the contract. Every once in a while we come across attorneys (typically not Queens’ real estate attorneys) who take it upon themselves to make
						        outrageous and unreasonable requests which can lead to big issues and possibly the deal being cancelled. If you find yourself in a situation where your attorney is doing this, you may want to switch attorneys asap.
