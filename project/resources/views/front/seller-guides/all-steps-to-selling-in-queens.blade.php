						    <h3>Step 18</h3>
						    <h3>Prepare to renegotiate after inspection</h3>
						    <p>
						        Now, you schedule the inspection, and since you did a pre-inspection, you should already be prepared as to what you can expect. The buyer’s inspection report comes back, and most likely they will try to renegotiate. You go back and
						        forth on a few items and then ideally find a win win so you can move on to the next step!
						    </p>
						    <p>If you get into a renegotiation after inspection and cannot come to an agreement, then the deal will die and you will need to start over with a new buyer (if you have back up offers) or back to showing if not.</p>
